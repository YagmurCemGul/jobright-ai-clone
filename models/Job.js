const mongoose = require('mongoose');

const JobSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  company: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  skills: {
    type: [String],
    required: true,
  },
  postedDate: {
    type: Date,
    default: Date.now,
  },
});

module.exports = Job = mongoose.model('job', JobSchema);